#include <iostream>
using namespace std;
typedef long long int ll;
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int n, odd = 0, even = 0, s = 0;
        cin >> n;
        int ar[n];
        for (int i = 0; i < n; i++)
        {
            cin >> ar[i];
            if (ar[i] & 1)
                odd++;
            else
                even++;
            s += ar[i];
        }
        if (s & 1 || (odd > 0 && even > 0))
            cout << "YES" << endl;
        else
            cout << "NO" << endl;
    }
    return 0;
}