#include <bits/stdc++.h>
using namespace std;
typedef long long int ll;
typedef long double ld;
int main()
{
    ll h, l;
    ld r;
    cin >> h >> l;
    r = (l * l - h * h) / (2.0 * h);
    cout << fixed << setprecision(13) << r << endl;
    return 0;
}