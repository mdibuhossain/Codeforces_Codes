#include <bits/stdc++.h>
using namespace std;
typedef long long int ll;
int main()
{
    ll n, texi = 0, ses[5] = {0};
    cin >> n;
    for (ll i = 0; i < n; i++)
    {
        ll k;
        cin >> k;
        ses[k]++;
    }
    texi += ses[4];
    if (ses[3] >= ses[1])
        ses[1] = 0;
    else
        ses[1] -= ses[3];
    if ((2 * ses[2] + ses[1]) % 4 == 0)
        texi += (2 * ses[2] + ses[1]) / 4;
    else
        texi += ((2 * ses[2] + ses[1]) / 4) + 1;
    texi += ses[3];
    cout << texi << endl;
    return 0;
}