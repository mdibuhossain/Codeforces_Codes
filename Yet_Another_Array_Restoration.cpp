#include <bits/stdc++.h>
using namespace std;
typedef long long int ll;
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int n, x, y;
        cin >> n >> x >> y;
        int sub = (y - x), l = n - 1;
        int d = l;
        while (d)
        {
            if (sub % d == 0)
                break;
            d--;
        }
        d = sub / d;
        int xx = n - (sub / d + 1), first = x;
        for (int i = 0; i < xx; i++)
        {
            if (first - d > 0)
                first -= d;
        }
        for (int i = 0; i < n; i++)
        {
            if (i == 0)
                cout << first;
            else
                cout << " " << first;
            first += d;
        }
        cout << endl;
    }
    return 0;
}