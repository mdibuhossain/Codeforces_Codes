#include <stdio.h>
int main()
{
    int n;
    scanf("%d", &n);
    if (n < 100)
    {
        if ((n % 10 == 4) || (n % 10 == 7 && (n / 10) % 10 == 4))
        {
            printf("YES\n");
        }
        else if (n % 4 == 0 || n % 7 == 0)
        {
            printf("YES\n");
        }
        else
        {
            printf("NO\n");
        }
    }
    else if (n <= 1000)
    {
        if ((n % 10 == 4 || n % 10 == 7) && ((n / 10) % 10 == 4 || (n / 10) % 10 == 7) && (n / 100 == 4 || n / 100 == 7))
            printf("YES\n");
        else if (n % 4 == 0 || n % 7 == 0)
        {
            printf("YES\n");
        }
        else
        {
            printf("NO\n");
        }
    }

    return 0;
}