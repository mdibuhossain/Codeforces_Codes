#include <bits/stdc++.h>
using namespace std;
typedef long long int ll;
int main()
{
    ll n, m, c = 0;
    cin >> n >> m;
    while (n != m)
    {
        if (n > m)
        {
            c += (n-m);
            break;
        }
        else
        {
            if (m & 1)
            {
                m += 1;
                c++;
            }
            else
            {
                m /= 2;
                c++;
            }
        }
    }
    cout << c << endl;
    return 0;
}