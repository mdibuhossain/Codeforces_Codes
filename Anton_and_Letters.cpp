#include <bits/stdc++.h>
using namespace std;
int main()
{
    string st;
    getline(cin, st);
    map<char, int> mp;
    for (int i = 0; i < st.length(); i++)
    {
        if (st[i] >= 'a' && st[i] <= 'z')
        {
            mp[st[i]]++;
        }
    }
    cout << mp.size() << endl;
    return 0;
}