#include <bits/stdc++.h>
using namespace std;
typedef long long int ll;
int main()
{
    int n;
    ll b = 0;
    cin >> n;
    while (n)
    {
        b += n % 2;
        n /= 2;
    }
    cout << b << endl;
    return 0;
}