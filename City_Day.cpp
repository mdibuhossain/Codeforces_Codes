#include <bits/stdc++.h>
using namespace std;
typedef long long int ll;
int main()
{
    int n, x, y, j = 1, d = -1, flag = 1;
    cin >> n >> x >> y;
    int a[n + 1];
    for (int i = 1; i <= n; i++)
    {
        cin >> a[i];
        if ((a[i] < a[j]) && flag)
            j = i;
        if ((i - j) >= y)
            flag = 0;
    }
    cout << j << endl;
    return 0;
}