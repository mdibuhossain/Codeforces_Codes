#include <bits/stdc++.h>
using namespace std;
typedef long long int ll;
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        ll n, sum = 0, frn = 0;
        cin >> n;
        ll a[n];
        for (ll i = 0; i < n; i++)
            cin >> a[i];
        sum += a[0];
        for (ll i = 1; i < n; i++)
        {
            if (a[i] == 0)
            {
                sum += frn / 3;
                frn = 0;
                continue;
            }
            frn++;
        }
        sum += frn / 3;
        cout << sum << endl;
    }
    return 0;
}