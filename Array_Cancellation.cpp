#include <bits/stdc++.h>
using namespace std;
typedef long long int ll;
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        ll nn, n = 0, p = 0;
        cin >> nn;
        for (int i = 0; i < nn; i++)
        {
            ll k;
            cin >> k;
            if (k >= 0)
                p += k;
            else
            {
                if (p + k >= 0)
                    p += k;
                else
                {
                    p += k;
                    n += p;
                    p = 0;
                }
            }
        }
        cout << p << endl;
    }
    return 0;
}