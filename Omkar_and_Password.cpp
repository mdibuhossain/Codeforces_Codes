#include <bits/stdc++.h>
using namespace std;
typedef long long int ll;
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int n;
        cin >> n;
        vector<ll> vt;
        for (int i = 0; i < n; i++)
        {
            ll k;
            cin >> k;
            vt.push_back(k);
        }
        sort(vt.begin(), vt.end());
        if (vt[0] == vt[n - 1])
            cout << n << endl;
        else
            cout << 1 << endl;
    }
    return 0;
}