#include <bits/stdc++.h>
using namespace std;
typedef long long int ll;
int main()
{
    ll n, sum = 0;
    cin >> n;
    ll ar[n + 1];
    for (ll i = 1; i <= n; i++)
        cin >> ar[i];
    sort(ar + 1, ar + n + 1);
    for (ll i = 1; i <= n; i++)
        sum += abs(ar[i] - i);
    cout << sum << endl;
    return 0;
}