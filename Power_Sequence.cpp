#include <bits/stdc++.h>
using namespace std;
typedef long long int ll;
ll m = LLONG_MAX;
ll lp = 9999999999;
int main()
{
    ll n, res = 0;
    cin >> n;
    ll a[n];
    for (ll i = 0; i < n; i++)
        cin >> a[i];
    sort(a, a + n);
    for (ll i = 1; pow(i, n - 1) <= lp; i++)
    {
        res = 0;
        for (ll j = 0; j < n; j++)
            res += abs(a[j] - pow(i, j));
        m = min(m, res);
    }
    cout << m << endl;
    return 0;
}