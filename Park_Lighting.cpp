#include <bits/stdc++.h>
using namespace std;
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int n, m, result;
        cin >> n >> m;
        if (n % 2 == 0)
            result = (n / 2) * m;
        else
        {
            if (m % 2 == 0)
                result = ((n / 2) * m) + (m / 2);
            else
                result = ((n / 2) * m) + ((m / 2) + 1);
        }
        cout << result << endl;
    }
    return 0;
}