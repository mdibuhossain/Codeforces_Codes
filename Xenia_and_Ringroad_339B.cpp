typedef long long int ll;

ll funFactorial(ll n)
{
    if (n == 0)
        return 1;
    return n * funFactorial(n - 1);
}

int main()
{
#ifndef ONLINE_JUDGE
    clock_t tStart = clock();
    freopen("input.txt", "r", stdin);
    freopen("output.txt", "w", stdout);
#endif

    ll n, m, tm = 0;
    cin >> n >> m;
    ll ar[m];
    for (ll i = 0; i < m; i++)
    {
        cin >> ar[i];
        if (i == 0)
            tm += ar[i] - 1;
        else
        {
            if (ar[i] != ar[i - 1])
            {
                if (ar[i] > ar[i - 1])
                    tm += ar[i] - ar[i - 1];
                else
                    tm += (n - ar[i - 1] + 1) + (ar[i] - 1);
            }
        }
    }
    cout << tm << endl;

#ifndef ONLINE_JUDGE
    fprintf(stderr, "\nRuntime: %.10fs\n", (double)(clock() - tStart) / CLOCKS_PER_SEC);
#endif
    return 0;
}