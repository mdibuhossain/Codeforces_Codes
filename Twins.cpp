#include <bits/stdc++.h>
using namespace std;
typedef long long int ll;
int main()
{
    ll n, sum = 0, s = 0, coin = 0;
    cin >> n;
    ll ar[n];
    for (ll i = 0; i < n; i++)
    {
        cin >> ar[i];
        sum += ar[i];
    }
    sort(ar, ar + n, greater<int>());
    for (int i = 0; i < n; i++)
    {
        s += ar[i];
        coin++;
        if (s > (sum-s))
            break;
    }
    cout << coin << endl;
    return 0;
}