#include <stdio.h>
#include <string.h>
#include <math.h>
#include <stdlib.h>
int main()
{
    int n, group_count = 1;
    scanf("%d", &n);
    int *ptr;
    ptr = (int *)malloc(n * sizeof(int));
    for (int i = 0; i < n; i++)
    {
        scanf("%d", &ptr[i]);
        if (i > 0)
        {
            if (ptr[i] != ptr[i - 1])
            {
                group_count++;
            }
        }
    }
    printf("%d\n", group_count);
    return 0;
}