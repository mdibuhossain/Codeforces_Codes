#include <bits/stdc++.h>
using namespace std;
typedef long long int ll;
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int dex, flag = 0, n;
        cin >> n;
        getchar();
        string st;
        cin >> st;
        if (st.size() < 8)
            cout << "NO" << endl;
        else
        {
            for (int i = 0; i < st.size(); i++)
            {
                if (st[i] == '8')
                {
                    dex = i;
                    flag = 1;
                    break;
                }
            }
            if (flag)
            {
                if ((st.size() - dex) >= 11)
                    cout << "YES" << endl;
                else
                    cout << "NO" << endl;
            }
            else
                cout << "NO" << endl;
        }
    }
    return 0;
}