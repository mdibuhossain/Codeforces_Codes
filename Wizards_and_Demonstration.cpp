#include <bits/stdc++.h>
using namespace std;
typedef long long int ll;
int main()
{
    ll n, x, y;
    cin >> n >> x >> y;
    double pc = (n * y) / 100.0;
    ll pc_int = pc;
    if (pc_int != pc)
        pc_int += 1;
    if(pc_int > x) cout << pc_int -x <<  endl;
    else cout << 0 << endl;
    return 0;
}   