#include <stdio.h>
#include <string.h>
#include <math.h>
#include <stdlib.h>
int main()
{
    int t, n;
    scanf("%d", &t);
    while (t--)
    {
        scanf("%d", &n);
        if (n == 1)
            printf("-1\n");
        else
        {
            printf("4");
            for (int i = 1; i < n; i++)
            {
                printf("7");
            }
            printf("\n");
        }
    }
    return 0;
}