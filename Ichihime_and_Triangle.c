#include <stdio.h>
#include <string.h>
#include <math.h>
#include <stdlib.h>
int main()
{
    int t;
    scanf("%d", &t);
    while (t--)
    {
        int a, b, c, d, z;
        scanf("%d%d%d%d", &a, &b, &c, &d);
        for (int i = c; i <= d; i++)
        {
            if ((b + c) > i)
            {
                z = i;
                break;
            }
        }

        printf("%d %d %d\n", b, c, z);
    }

    return 0;
}