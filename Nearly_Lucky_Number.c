#include <stdio.h>
#include <string.h>
#include <math.h>
#include <stdlib.h>
typedef long long int ll;
int main()
{
    int lucy = 0;
    char s[20];
    gets(s);
    for (int i = 0; i < strlen(s); i++)
    {
        if (s[i] == '4' || s[i] == '7')
        {
            lucy++;
        }
    }
    if (lucy == 4 || lucy == 7)
        printf("YES\n");
    else
        printf("NO\n");
    return 0;
}