#include <bits/stdc++.h>
using namespace std;
typedef long long int ll;
int main()
{
#ifndef ONLINE_JUDGE
    freopen("input.txt", "r", stdin);
    freopen("output.txt", "w", stdout);
#endif
    ll t;
    cin >> t;
    while (t--)
    {
        ll x, res = 0, sum = 0;
        cin >> x;
        for (ll i = 1, j = 2; 1; j++)
        {
            ll y = (i * (i + 1)) / 2;
            sum += y;
            if (sum > x)
                break;
            res++;
            i = (pow(2, j)) - 1;
        }
        cout << res << endl;
    }
    return 0;
}