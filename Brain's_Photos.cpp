#include<bits/stdc++.h>
using namespace std;
typedef long long int ll;
int main()
{
 	int n, m, cl = 0;
 	cin >> n >> m;
 	char a[n][m];
 	for (int i = 0; i < n; ++i)
 	{
 		for (int j = 0; j < m; ++j)
 		{
 			cin >> a[i][j];
 			if(a[i][j] == 'C' || a[i][j] == 'M' || a[i][j] == 'Y')
 				cl = 1;
 		}
 	}
 	if(cl)
 		cout << "#Color" << endl;
 	else
 		cout << "#Black&White" << endl;
    return 0;
}