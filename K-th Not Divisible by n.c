#include <stdio.h>
#include <string.h>
#include <math.h>
#include <stdlib.h>
int main()
{
    int t;
    scanf("%d", &t);
    while (t--)
    {
        int i, n, k, count = 0;
        scanf("%d%d", &n, &k);
        for (i = 1; 1; i++)
        {
            if (i % n != 0)
            {
                count++;
            }
            if (count == k)
            {
                break;
            }
        }
        printf("%d\n", i);
    }
    return 0;
}