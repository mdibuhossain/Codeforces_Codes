#include<bits/stdc++.h>
using namespace std;
typedef long long int ll;
int main()
{
    int t;
    cin >> t;
    while(t--)
    {
        ll n, k;
        cin >> n >> k;
        if(n == k)
            cout << 0 << endl;
        else if(n < k)
            cout << (k-n) << endl;
        else if(n > k)
        {
            if(n&1)
                cout << 1 << endl;
            else
                cout << 0 << endl;
        }   
    }
    return 0;
}