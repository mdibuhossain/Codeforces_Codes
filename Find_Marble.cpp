#include <bits/stdc++.h>
using namespace std;
typedef long long int ll;
int main()
{
    ll n, s, t, c = 0;
    cin >> n >> s >> t;
    ll ar[n+1];
    for (int i = 1; i <= n; i++) cin >> ar[i];
    while (s != t)
    {
        s = ar[s];
        c++;
        if(c > n){c = -1; break;} 
    }
    cout << c << endl;
    return 0;
}