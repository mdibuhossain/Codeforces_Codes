#include <bits/stdc++.h>
using namespace std;
typedef long long int ll;
int main()
{
    ll n, ma = 1, count = 1;
    cin >> n;
    ll a[n];
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
        if (i > 0)
        {
            if (a[i - 1] <= a[i])
                count++;
            else
            {
                ma = max(ma, count);
                count = 1;
            }
        }
    }
    cout << max(ma, count) << endl;
    return 0;
}