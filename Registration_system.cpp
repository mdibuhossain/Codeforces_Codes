#include <bits/stdc++.h>
using namespace std;
int main()
{
    int t;
    cin >> t;
    string st;
    map<string, int> m;
    for (int i = 0; i < t; i++)
    {
        cin >> st;
        m[st]++;
        if (m[st] > 1)
        {
            cout << st << m[st] - 1 << endl;
        }
        else
        {
            cout << "OK" << endl;
        }
    }
    return 0;
}